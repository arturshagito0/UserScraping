        def executeScrapeRequest(self):
                errorMessage = ""
                error = False
                groupNo = self.groupNo.text()
                if not groupNo or any(item in groupNo for item in string.ascii_lowercase):
                        errorMessage += "Please enter valid group number\n"
                        error = error or True

                scrapePercent = self.scrapePercentage.text()
                if not scrapePercent:
                        errorMessage += "Please enter valid scrape percentage"
                        error = error or True
                else:
                        scrapePercent = float(scrapePercent)

                        if scrapePercent < 0 or scrapePercent > 1:
                                errorMessage += "Please enter valid scrape percentage\n"
                                error = error or True
                        else:
                                error = error or False

                if error:
                        self.raiseError(errorMessage)
                else:

                        location = self.locationNeeded.isChecked()
                        self.requestsList.addItem(self.groupNo_2.text())
                        print(
                                f"{groupNo} is the group number and {scrapePercent} as fraction, location needed: {location}")

                        newRequest = self.ScrapeRequest(self, self.groupNo_2.text(), int(groupNo), float(scrapePercent),
                                                        location)
                        self.pendingRequests.append(newRequest)
                        newRequest.execute()
                        self.pendingRequests.remove(newRequest)
                        self.finishedRequests.append(newRequest)

